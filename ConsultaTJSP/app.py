historico_consultas = []

def normalizar(texto):
    texto = str(texto).strip().lower()
    return unicodedata.normalize("NFKD", texto).encode("ASCII", "ignore").decode("ASCII")

def buscar_zona(entrada):
    entrada_normalizada = normalizar(entrada)
    for chave, zona in enderecos_zonas.items():
        if entrada_normalizada in normalizar(chave):
            return chave, zona
    return None, None

@app.route("/")
def index():
    return render_template("index.html", resultado=None)

@app.route("/consultar_zona", methods=["POST"])
def consultar_zona():
    entrada = request.form["entrada"]
    chave, zona = buscar_zona(entrada)
    if zona:
        resultado = f"Endereço localizado: {chave}<br>Zona Judicial: {zona}"
    else:
        resultado = "Endereço não encontrado."
    historico_consultas.append({
        "data": datetime.now().strftime("%d/%m/%Y %H:%M"),
        "tipo": "Zona",
        "entrada": entrada,
        "resultado": resultado
    })
    return render_template("index.html", resultado=resultado)

@app.route("/consultar_comarca", methods=["POST"])
def consultar_comarca():
    codigo = request.form["codigo"].zfill(4)
    resultado = comarcas.get(codigo)
    if resultado:
        resposta = f"Código {codigo} → Comarca: {resultado}"
    else:
        resposta = "Comarca não encontrada."
    historico_consultas.append({
        "data": datetime.now().strftime("%d/%m/%Y %H:%M"),
        "tipo": "Comarca",
        "entrada": codigo,
        "resultado": resposta
    })
    return render_template("index.html", resultado=resposta)

